package CG;

public class Illumination {
    double x;
    double y;
    double z;
    double R;
    double G;
    double B;

    public Illumination(double x, double y, double z, double R, double G, double B) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.R = R;
        this.G = G;
        this.B = B;
    }
    
    
}
